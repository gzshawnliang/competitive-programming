#include <bits/stdc++.h>

using namespace std;

ifstream fin("MonkeysintheEmeiMountain.in");
//ofstream fout("MonkeysintheEmeiMountain.out");

int main()
{
    while (true)
    {
        int n = 0;
        fin >> n;
        if (n == 0)
        {
            break;
        }
        int m = 0;
        fin >> m;
        for (int i = 0; i <= n - 1; ++i)
        {
            int v, a, b;
            fin >> v >> a >> b;
        }
    }

    return 0;
}
